    public int getDestino()
    {
        Object o = jComboBoxDestino.getSelectedItem();
        String identificador;
        String strPosicao;

        if ( o instanceof String )
        {
            identificador = (String) o;
            // Ex.: "12. Paulo" --> "12":
            strPosicao = identificador.replaceFirst( "\\. .*", "");
            return Integer.parseInt( strPosicao );
        }
        // A linha seguinte não deverá ser acionada.
        else
            return 0;
    }

    /**
     * 
     * @return
     */
    public List<Integer> getPosicoes()
    {
        List<Integer> posicoes = new ArrayList<Integer>();
        Object[] objetos = jListComandas.getSelectedValues();
        CheckListItem cli;
        String identificador;
        String strPosicao;
        int posicao;

        for ( Object o : objetos)
        {
            if ( o instanceof CheckListItem )
            {
                cli = ( CheckListItem ) o;
                identificador = cli.toString();
                // Ex.: "12. Paulo" --> "12":
                strPosicao = identificador.replaceFirst( "\\. .*", "");
                posicao = Integer.parseInt( strPosicao );
                posicoes.add( posicao );
            }
        }
        return posicoes;
    }
    
