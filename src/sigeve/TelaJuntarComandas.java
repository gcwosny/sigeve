    private void carregarComandas()
    {
        try
        {
            String identificador;
            Comanda comandaAberta;
            Iterator<Comanda> it = bd.listarComandasAbertasBD().iterator();

            while ( it.hasNext() )
            {
                comandaAberta = it.next();
                identificador = comandaAberta.getPosicao() + ". " + comandaAberta.getNome();
                comboBoxModelDestino.addElement( identificador );
                //listModelComandas.addElement( identificador );
                listModelComandas.addElement( new CheckListItem( identificador ) );
            }
        }
        catch ( SQLException se )
        {
            Logger.getLogger( SigeveView.class.getName() ).log( Level.SEVERE, null, se );
        }
    }

